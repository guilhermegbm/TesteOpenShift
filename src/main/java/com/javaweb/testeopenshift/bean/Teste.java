package com.javaweb.testeopenshift.bean;

/**
 *
 * @author Guilherme
 */
public class Teste {
    private int codigo;
    private String nome;
    private String valor;

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getValor() {
        return valor;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }

    @Override
    public String toString() {
        return "Teste:" + " codigo=" + codigo + ", nome=" + nome + ", valor=" + valor;
    }
}
